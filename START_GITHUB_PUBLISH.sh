【方法2】手動で実行
─────────────────────────────────────────────
# 1. Gitリポジトリ初期化
git init

# 2. ファイル追加
git add .

# 3. 初回コミット
git commit -m "feat: 初回コミット - カービィ風RPGの基本実装"

# 4. ブランチ名を設定
git branch -M main

# 5. リモートリポジトリ追加
git remote add origin https://github.com/remmakoshino/kirby-inspired-go.git

# 6. プッシュ
git push -u origin main


